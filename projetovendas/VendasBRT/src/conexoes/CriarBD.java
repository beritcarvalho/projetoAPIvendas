    private boolean gravarConfiguracao() {
        boolean grava = false;
        try {
            File diretorio = new File("C:/ConexaoBanco/Configuracao");
            if (!diretorio.exists()) {
                diretorio.mkdirs();
            }
            FileWriter arq1 = new FileWriter("C:/ConexaoBanco/Configuracao/conexao.ini");
            PrintWriter gravarArq = new PrintWriter(arq1);
            gravarArq.println(txtHost.getText());
            gravarArq.println(txtPorta.getText());
            gravarArq.println(txtUser.getText());
            gravarArq.println(pswPassword.getPassword());
            arq1.close();
            grava = true;
        } catch (IOException erro) {
            JOptionPane.showMessageDialog(null, "Você nao tem permição de adminstrador para configurar o"
                    + " Sistema.", "ERRO !", 0);
        }
        return grava;        
    }

    private boolean lerConfiguracaoGravada() {
        
             
        boolean leitura = false;
        try {
            String linha;
            String ArquivoConfiguracao = "C:/ConexaoBanco/Configuracao/conexao.ini";
            int cont = 0;
            File arq = new File(ArquivoConfiguracao);
            if (arq.exists()) {

                FileReader reader = new FileReader(ArquivoConfiguracao);
                BufferedReader leitor = new BufferedReader(reader);
                while (true) {
                    linha = leitor.readLine();
                    if (cont == 0) {
                        txtHost.setText(linha);
                    }
                    if (cont == 1) {
                        txtPorta.setText(linha);
                    }
                    if (cont == 2) {
                        txtUser.setText(linha);
                    }
                    if (cont >= 3) {
                        pswPassword.setText(linha);
                        String conteudoPassword = new String(pswPassword.getPassword());
                        conteudoPassword.toString();
                        break;
                    }
                    System.out.println(linha + "\n");
                    cont++;
                }
            }
            if (arq.exists()) {
                
                if (conexaoServidor(txtHost.getText(), txtPorta.getText(), txtUser.getText(), pswPassword.getPassword().toString())) {
                    leitura = true;
                }
            }

        } catch (Exception erro) {
        }

        return leitura;
    }
    
    private boolean conexaoServidor(String pServidor, String pPorta, String pUsuario, String pSenha) {
        boolean con = false;
        String servidor = pServidor;
        String portaConexao = pPorta;
        String usuario = pUsuario;
        String senha = pSenha;
        try {
            Class.forName(driver);
            conexao = DriverManager.getConnection("jdbc:mysql://" + servidor + ":" + portaConexao + "",
                    usuario, senha);
            con = true;
        } catch (ClassNotFoundException Fonte) {
            JOptionPane.showMessageDialog(null, "Driver nao localizado");
        } catch (SQLException Fonte) {
        }
        return con;
    }
    
    private boolean conexaoDataBase(String pServidor, String pPorta, String pUsuario, String pSenha) {
    boolean con = false;
    String servidor = pServidor;
    String portaConexao = pPorta;
    String usuario = pUsuario;
    String senha = pSenha;
    try {
        Class.forName(driver);
        conexao = DriverManager.getConnection("jdbc:mysql://" + servidor + ":" + portaConexao
                + "/bd_Brt_Vendas", usuario, senha);
        con = true;
    } catch (ClassNotFoundException Fonte) {
        JOptionPane.showMessageDialog(null, "Driver nao localizado");
    } catch (SQLException Fonte) {
    }
    return con;
}

    private void fechaConexao() {
        try {
            conexao.close();
        } catch (SQLException fech) {
            JOptionPane.showMessageDialog(null, "Erro ao fechar conexao com o banco de dados" + fech);
        }
    }
    
    private boolean criaDataBaseCompleto() {
        boolean sucesso = true;
        if (criaBanco() == false) {
            sucesso = false;
        } else if (criaTabela() == false) {
            sucesso = false;
        }

        return sucesso;
    }

    private boolean criaBanco() {
        boolean sucesso = false;
        PreparedStatement stm;
        String sql = "CREATE DATABASE IF NOT EXISTS bd_Brt_Vendas";

        try {
            stm = conexao.prepareStatement(sql);
            stm.execute();
            sucesso = true;
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", 0);
        }
    //        FECHA_CONEXAO();
        return sucesso;
    }
    
    private boolean criaTabelas() {
        boolean sucesso = true;

        conexaoDataBase(txtHost.getText(), txtPorta.getText(), txtUser.getText(), pswPassword.getPassword().toString());

        if (CRIA_TABELA_MODELO() == false) {
            sucesso = false;
        } else if (CRIA_TABELA_COR() == false) {
            sucesso = false;
        } else if (CRIA_TABELA_CARRO() == false) {
            sucesso = false;
        }

        fechaConexao();
        return sucesso;
    }    
    
    
